class Solution {
  public:
    Node* constructLL(vector<int>& nums) {
        Node* head=NULL;
        Node* tail=NULL;
        
        head=new Node(nums[0]);
        
        tail=head;
        for(int i=1;i<nums.size();i++){
            Node* temp=new Node(nums[i]);
            tail->next=temp;
            tail=tail->next;
        }
      return head;
    }
};

